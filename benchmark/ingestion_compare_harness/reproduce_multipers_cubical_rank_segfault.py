def _extract_payload(res) -> tuple[np.ndarray, np.ndarray]:
    if isinstance(res, tuple):
        return np.asarray(res[0]), np.asarray(res[1])
    if isinstance(res, list):
        if not res:
            return np.empty((0, 2), dtype=np.float64), np.empty((0,), dtype=np.float64)
        if len(res) != 1:
            raise RuntimeError(f"Unexpected multipers signed_measure list length: {len(res)}")
        return np.asarray(res[0][0]), np.asarray(res[0][1])
    raise RuntimeError(f"Unexpected signed_measure return type: {type(res)!r}")


def main() -> int:
    parser = argparse.ArgumentParser(description="Reproduce the multipers cubical rank segfault on the 8x8 image_sine fixture.")
    parser.add_argument("--degree", type=int, default=0, help="Homology degree passed to mp.signed_measure.")
    args = parser.parse_args()

    try:
        import multipers as mp  # type: ignore
        import multipers.filtrations as mf  # type: ignore
    except Exception as exc:
        print(f"Failed to import multipers: {exc}", file=sys.stderr)
        return 2

    image = _IMAGE_SINE_8X8_SEED11.copy()
    print(f"multipers={getattr(mp, '__version__', 'unknown')}", flush=True)
    print(f"image_shape={image.shape}", flush=True)

    cubical = mf.Cubical(image[:, :, None])
    print(f"cubical_type={type(cubical).__name__}", flush=True)

    grid = mp.grids.compute_grid(cubical, strategy="exact")
    print(f"grid_sizes={[len(np.asarray(axis)) for axis in grid]}", flush=True)
    print("calling mp.signed_measure(..., invariant='rank')", flush=True)

    res = mp.signed_measure(
        cubical,
        degree=args.degree,
        invariant="rank",
        grid=grid,
        plot=False,
        mass_default=None,
        zero_pad=False,
        expand_collapse=False,
    )

    print("rank call returned", flush=True)
    locs, weights = _extract_payload(res)
    print(f"locs_shape={tuple(locs.shape)}", flush=True)
    print(f"weights_shape={tuple(weights.shape)}", flush=True)
    print(f"n_terms={int(weights.size)}", flush=True)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
