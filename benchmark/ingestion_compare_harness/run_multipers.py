#!/usr/bin/env python3
"""
Run multipers ingestion benchmark on fixtures generated by generate_fixtures.py.
"""

from __future__ import annotations

import argparse
import csv
import datetime as dt
import gc
import statistics
import time
from pathlib import Path
from typing import Any
import tomllib

import numpy as np


def _profile_defaults(profile: str) -> dict[str, int]:
    profile = profile.lower()
    if profile == "desktop":
        # Desktop-safe default: slightly lower reps + aggressive memory trimming.
        return {"reps": 4}
    if profile == "balanced":
        # Balanced local runs with more stable medians.
        return {"reps": 5}
    if profile == "stress":
        # Heavier sampling for publication-grade runs.
        return {"reps": 9}
    if profile == "probe":
        # Quick probe for fast feedback.
        return {"reps": 3}
    raise ValueError("--profile must be one of: desktop, balanced, stress, probe")


def _memory_relief() -> None:
    gc.collect()
    try:
        import ctypes

        ctypes.CDLL("libc.so.6").malloc_trim(0)
    except Exception:
        # Non-glibc platforms may not provide malloc_trim.
        pass


def _try_import_multipers():
    try:
        import gudhi as gd  # type: ignore
        import multipers as mp  # type: ignore
        import multipers.filtrations as mf  # type: ignore
        import multipers.ml.point_clouds as mmp  # type: ignore
    except Exception as exc:  # pragma: no cover
        raise RuntimeError(
            "multipers is not available. Install it in the active Python environment."
        ) from exc
    return mmp, mp, gd, mf


def _load_manifest(path: Path) -> list[dict[str, Any]]:
    raw = tomllib.loads(path.read_text(encoding="utf-8"))
    cases = raw.get("cases", [])
    if not isinstance(cases, list) or not cases:
        raise ValueError(f"No cases found in {path}")
    return cases


def _timed_call(f):
    t0 = time.perf_counter()
    out = f()
    t1 = time.perf_counter()
    return out, 1000.0 * (t1 - t0)


def _p90(vals: list[float]) -> float:
    if not vals:
        return float("nan")
    s = sorted(vals)
    idx = max(0, int(np.ceil(0.9 * len(s))) - 1)
    return s[idx]


def _simplex_count(st) -> int | None:
    for name in ("num_simplices", "n_simplices", "simplex_count"):
        if hasattr(st, name):
            v = getattr(st, name)
            try:
                return int(v() if callable(v) else v)
            except Exception:
                pass
    return None


def _load_case_data(case: dict[str, Any], fixture: Path) -> np.ndarray:
    dataset = str(case["dataset"])
    data = np.loadtxt(fixture, delimiter=",", dtype=np.float64)
    if dataset == "gaussian_shell":
        if data.ndim == 1:
            data = data.reshape(-1, 1)
        return data
    if dataset == "image_sine":
        if data.ndim != 2:
            raise RuntimeError(f"image_sine fixture must be 2D matrix, got shape={data.shape}.")
        return data
    raise RuntimeError(f"Unsupported dataset: {dataset}")


def _build_multipers_simplextree(mmp, mp, gd, mf, data: np.ndarray, case: dict[str, Any]):
    regime = str(case["regime"])
    max_dim = int(case["max_dim"])
    points = data
    common = {
        "complex": "rips",
        "output_type": "simplextree",
        # Match tamer-op's max_dim semantics (max simplex dimension).
        "expand_dim": max_dim,
    }
    if regime == "claim_matching":
        if "claim_radius" not in case:
            raise RuntimeError("claim_matching case requires claim_radius in manifest.")
        claim_radius = float(case["claim_radius"])
        st0 = gd.RipsComplex(points=points, max_edge_length=claim_radius).create_simplex_tree(max_dimension=max_dim)
        st = mp.simplex_tree_multi.SimplexTreeMulti(st0, num_parameters=1, safe_conversion=False)
        return st, {"builder": "gudhi_rips_radius", "radius": claim_radius, "max_dim": max_dim}
    elif regime == "normalized_parity":
        if "parity_radius" in case:
            parity_radius = float(case["parity_radius"])
            st0 = gd.RipsComplex(points=points, max_edge_length=parity_radius).create_simplex_tree(max_dimension=max_dim)
            st = mp.simplex_tree_multi.SimplexTreeMulti(st0, num_parameters=1, safe_conversion=False)
            return st, {"builder": "gudhi_rips_radius", "radius": parity_radius, "max_dim": max_dim}
        tries = [
            {
                **common,
                "num_collapses": 0,
            },
            {
                **common,
            },
        ]
    elif regime == "degree_rips_parity":
        if "degree_radius" not in case:
            raise RuntimeError("degree_rips_parity case requires degree_radius in manifest.")
        degree_radius = float(case["degree_radius"])
        try:
            st = mf.DegreeRips(points=points, threshold_radius=degree_radius)
            return st, {"builder": "filtrations.DegreeRips", "threshold_radius": degree_radius, "max_dim": max_dim}
        except NotImplementedError:
            # Some multipers builds expose DegreeRips but raise NotImplementedError.
            # Emulate degree-Rips via RipsLowerstar using threshold-graph vertex degree.
            st0 = gd.RipsComplex(points=points, max_edge_length=degree_radius).create_simplex_tree(max_dimension=1)
            deg = np.zeros(points.shape[0], dtype=np.float64)
            for simplex, _ in st0.get_skeleton(1):
                if len(simplex) != 2:
                    continue
                u, v = int(simplex[0]), int(simplex[1])
                deg[u] += 1.0
                deg[v] += 1.0
            st = mf.RipsLowerstar(points=points, function=deg, threshold_radius=degree_radius)
            return st, {
                "builder": "emulated_degree_via_ripslowerstar",
                "threshold_radius": degree_radius,
                "max_dim": max_dim,
            }
    elif regime == "rips_codensity_parity":
        if "codensity_radius" not in case:
            raise RuntimeError("rips_codensity_parity case requires codensity_radius in manifest.")
        codensity_radius = float(case["codensity_radius"])
        dtm_mass = case.get("codensity_dtm_mass")
        dtm_mass_val = None if dtm_mass is None else float(dtm_mass)
        st = mf.RipsCodensity(points, dtm_mass=dtm_mass_val, threshold_radius=codensity_radius)
        return st, {
            "builder": "filtrations.RipsCodensity",
            "threshold_radius": codensity_radius,
            "dtm_mass": dtm_mass_val,
            "max_dim": max_dim,
        }
    elif regime == "rips_lowerstar_parity":
        if "lowerstar_radius" not in case:
            raise RuntimeError("rips_lowerstar_parity case requires lowerstar_radius in manifest.")
        lowerstar_radius = float(case["lowerstar_radius"])
        fvals = np.asarray(points[:, 0], dtype=np.float64)
        st = mf.RipsLowerstar(points=points, function=fvals, threshold_radius=lowerstar_radius)
        return st, {
            "builder": "filtrations.RipsLowerstar",
            "threshold_radius": lowerstar_radius,
            "function": "coord1",
            "max_dim": max_dim,
        }
    elif regime == "delaunay_lowerstar_parity":
        fvals = np.asarray(points[:, 0], dtype=np.float64)
        st = mf.DelaunayLowerstar(points, fvals, verbose=False)
        return st, {"builder": "filtrations.DelaunayLowerstar", "function": "coord1", "max_dim": max_dim}
    elif regime == "alpha_function_delaunay":
        # Alpha parity: multipers alpha-complex vs tamer alpha filtration.
        tries = [
            {
                "complex": "alpha",
                "output_type": "simplextree",
                "expand_dim": max_dim,
                "num_collapses": 0,
                "safe_conversion": False,
            },
            {
                "complex": "alpha",
                "output_type": "simplextree",
                "expand_dim": max_dim,
                "safe_conversion": False,
            },
        ]
    elif regime == "landmark_parity":
        if "landmarks" not in case:
            raise RuntimeError("landmark_parity case requires landmarks in manifest.")
        if "landmark_radius" not in case:
            raise RuntimeError("landmark_parity case requires landmark_radius in manifest.")
        landmarks_1 = np.asarray(case["landmarks"], dtype=np.int64)
        if landmarks_1.size == 0:
            raise RuntimeError("landmark_parity landmarks cannot be empty.")
        # Manifest stores 1-based indices for Julia; convert to 0-based here.
        idx0 = landmarks_1 - 1
        if idx0.min() < 0 or idx0.max() >= points.shape[0]:
            raise RuntimeError("landmark_parity landmarks are out of bounds for fixture point cloud.")
        sub = points[idx0, :]
        radius = float(case["landmark_radius"])
        st0 = gd.RipsComplex(points=sub, max_edge_length=radius).create_simplex_tree(max_dimension=max_dim)
        st = mp.simplex_tree_multi.SimplexTreeMulti(st0, num_parameters=1, safe_conversion=False)
        return st, {"builder": "gudhi_landmark_rips_radius", "radius": radius, "max_dim": max_dim, "n_landmarks": int(sub.shape[0])}
    elif regime == "core_delaunay_parity":
        ks = case.get("core_ks")
        ks_vals = None if ks is None else [int(v) for v in ks]
        st = mf.CoreDelaunay(points, ks=ks_vals, precision="safe", verbose=False)
        # Align simplex-dimension cap with case max_dim.
        st.prune_above_dimension(max_dim)
        return st, {"builder": "filtrations.CoreDelaunay", "max_dim": max_dim, "ks": ks_vals}
    elif regime == "cubical_parity":
        image = data
        if image.ndim != 2:
            raise RuntimeError(f"cubical_parity expects 2D image fixture, got shape={image.shape}")
        slicer = mf.Cubical(image[:, :, None])
        return slicer, {"builder": "filtrations.Cubical", "image_shape": tuple(int(v) for v in image.shape)}
    else:
        raise ValueError(f"Unsupported regime: {regime}")

    last_err: Exception | None = None
    for kw in tries:
        try:
            est = mmp.PointCloud2FilteredComplex(**kw)
            out = est.fit_transform([points])
            if len(out) == 0 or len(out[0]) == 0:
                raise RuntimeError("PointCloud2FilteredComplex returned empty output.")
            st = out[0][0]
            return st, kw
        except Exception as exc:
            last_err = exc
            continue
    assert last_err is not None
    raise RuntimeError(f"All multipers constructor attempts failed for regime={regime}") from last_err


def _to_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    cols = [
        "tool",
        "case_id",
        "regime",
        "n_points",
        "ambient_dim",
        "max_dim",
        "cold_ms",
        "cold_alloc_kib",
        "warm_median_ms",
        "warm_p90_ms",
        "warm_alloc_median_kib",
        "simplex_count",
        "edge_count",
        "max_simplex_dim",
        "notes",
        "timestamp_utc",
    ]
    with path.open("w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        for r in rows:
            w.writerow(r)


def main() -> None:
    p = argparse.ArgumentParser(description="Run multipers ingestion benchmark from fixtures.")
    p.add_argument("--manifest", type=Path, default=Path(__file__).with_name("fixtures") / "manifest.toml")
    p.add_argument("--out", type=Path, default=Path(__file__).with_name("results_multipers.csv"))
    p.add_argument("--profile", type=str, default="desktop")
    p.add_argument("--reps", type=int, default=None)
    p.add_argument("--regime", type=str, default="all")
    p.add_argument("--case", type=str, default="")
    args = p.parse_args()

    defaults = _profile_defaults(args.profile)
    reps = int(args.reps) if args.reps is not None else defaults["reps"]
    if reps < 1:
        raise ValueError("--reps must be >= 1")

    profile_name = args.profile.lower()
    trim_between_reps = profile_name == "desktop"
    trim_between_cases = profile_name in {"desktop", "balanced", "probe"}
    print(
        f"[profile] {profile_name} "
        f"(reps={reps}, trim_between_reps={trim_between_reps}, trim_between_cases={trim_between_cases})"
    )
    mmp, mp, gd, mf = _try_import_multipers()
    cases = _load_manifest(args.manifest)
    rows: list[dict[str, Any]] = []

    for c in cases:
        case_id = str(c["id"])
        regime = str(c["regime"])
        if args.regime != "all" and args.regime != regime:
            continue
        if args.case and args.case != case_id:
            continue

        fixture = args.manifest.parent / str(c["path"])
        data = _load_case_data(c, fixture)

        n_points = int(c["n_points"])
        ambient_dim = int(c["ambient_dim"])
        max_dim = int(c["max_dim"])

        notes = ""
        chosen = {}
        try:
            # Harness policy: algorithmic runtime baseline.
            # Prewarm once untimed to remove first-hit/import/JIT effects.
            _build_multipers_simplextree(mmp, mp, gd, mf, data, c)
            if trim_between_reps:
                _memory_relief()
            st_cold, cold_ms = _timed_call(lambda: _build_multipers_simplextree(mmp, mp, gd, mf, data, c))
            st, chosen = st_cold

            warm_times: list[float] = []
            for _ in range(reps):
                (_, _), tms = _timed_call(lambda: _build_multipers_simplextree(mmp, mp, gd, mf, data, c))
                warm_times.append(tms)
                if trim_between_reps:
                    _memory_relief()
        except Exception as exc:
            print(f"[skip] {case_id:28s} regime={regime} reason={type(exc).__name__}: {exc}")
            if trim_between_cases:
                _memory_relief()
            continue

        cold_alloc_kib = ""
        warm_alloc_kib = ""
        simplex_count = _simplex_count(st)
        max_simplex_dim = int(c["max_dim"])
        notes = f"kwargs={chosen};cold_mode=warm_uncached"

        print(
            f"{case_id:28s} regime={regime} cold_ms={cold_ms:.3f} warm_med_ms={statistics.median(warm_times):.3f} simplices={simplex_count}"
        )

        rows.append(
            {
                "tool": "multipers",
                "case_id": case_id,
                "regime": regime,
                "n_points": n_points,
                "ambient_dim": ambient_dim,
                "max_dim": max_dim,
                "cold_ms": cold_ms,
                "cold_alloc_kib": cold_alloc_kib,
                "warm_median_ms": statistics.median(warm_times),
                "warm_p90_ms": _p90(warm_times),
                "warm_alloc_median_kib": warm_alloc_kib,
                "simplex_count": simplex_count if simplex_count is not None else "",
                "edge_count": "",
                "max_simplex_dim": max_simplex_dim,
                "notes": notes,
                "timestamp_utc": dt.datetime.now(dt.timezone.utc).isoformat(),
            }
        )
        if trim_between_cases:
            _memory_relief()

    if not rows:
        raise RuntimeError("No cases selected. Check --manifest/--regime/--case.")

    _to_csv(args.out, rows)
    print(f"Wrote multipers results: {args.out}")


if __name__ == "__main__":
    main()
